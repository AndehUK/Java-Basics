package net.andeh;

public class Main {

    public static void main(String[] args) {
        int x = 100;
        int y = 20;

        System.out.println(x++);    //x = x + 1
        System.out.println(x--);    //x = x - 1
        System.out.println(x += y); //x = x + y
        System.out.println(x -= y); //x = x - y
        System.out.println(x *= y); //x = x * y
        System.out.println(x /= y); //x = x / y
        System.out.println(x %= y); //x = x % y */
    }
}
